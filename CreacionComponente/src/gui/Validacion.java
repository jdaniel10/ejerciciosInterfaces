    public static void validacion(String tipoValidar, JLabel Label,JTextField TextField){//Inicio validacion
            if (tipoValidar == "cp") {
                if (TextField.getText().length()!=5) {
                    Label.setText("Error, no tiene 5 digitos");
                }else{
                    Label.setText("");
                }
            } else if(tipoValidar == "dni") {
                if (TextField.getText().length()!=9)
                {
                    Label.setText("Error, no tiene 9 digitos");
                }else{
                    Label.setText("");
                }
            }
            else if (tipoValidar == "correo"){
                if (!TextField.getText().contains("@"))
                {
                    Label.setText("Error, no tiene @");
                }else{
                    Label.setText("");
                }
            }
        }
